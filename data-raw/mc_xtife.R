# ==============================================================================
# SECTION 6 ŌĆö OLS vs NNR Initialization Comparison
# ==============================================================================
#
# DGP designed so OLS init is demonstrably inferior:
#   fill = 0.5  (50% missing ŌĆö extreme unbalancedness)
#   r_true = 3  (richer factor structure)
#   SNR = var(lambda'f) / var(u) = 10  (strong signal: lambda_sd = sqrt(3))
#   N = 50, T = 50
#
# Why NNR wins: OLS ignores factors completely; with 50% fill and strong factors
# beta_hat_OLS is far from truth, requiring many outer iterations.
# NNR starts at the low-rank imputed solution, already near the fixed point.
# --------------------------------------------------------------------------

run_ols_vs_nnr <- function(N, TT, r, beta, B, nc,
                            fill           = 0.5,
                            snr_lambda_sd  = sqrt(3),   # sd of lambda_i
                            seed0          = 42L) {

  results <- parallel::mclapply(seq_len(B), function(b) {
    set.seed(seed0 + b)

    # High-SNR DGP (factors very strong)
    F_true <- matrix(rnorm(TT * r), TT, r)
    Lam    <- matrix(rnorm(N * r) * snr_lambda_sd, N, r)
    # factor component in (unit,time) expand.grid order
    fc     <- as.vector(Lam %*% t(F_true))    # N x TT -> as.vector = column-wise

    df_full <- expand.grid(unit = seq_len(N), time = seq_len(TT))
    df_full$X <- rnorm(N * TT) + fc * 0.5    # X correlated with factors
    df_full$Y <- beta * df_full$X + fc + rnorm(N * TT, sd = 0.5)

    keep <- sort(sample(nrow(df_full), floor(fill * nrow(df_full))))
    df   <- df_full[keep, ]
    idx  <- c("unit", "time")

    tryCatch({
      f_ols <- ife_unbalanced(Y ~ X, data = df, index = idx,
                              r = r, init = "ols")
      f_nnr <- ife_unbalanced(Y ~ X, data = df, index = idx,
                              r = r, init = "nnr")
      c(b_ols    = unname(f_ols$coef["X"]),
        b_nnr    = unname(f_nnr$coef["X"]),
        iter_ols = f_ols$n_iter,
        iter_nnr = f_nnr$n_iter,
        conv_ols = as.integer(f_ols$converged),
        conv_nnr = as.integer(f_nnr$converged),
        diff     = abs(f_ols$coef["X"] - f_nnr$coef["X"]))
    }, error = function(e) rep(NA_real_, 7L))

  }, mc.cores = nc, mc.set.seed = FALSE)

  mat <- do.call(rbind, results)
  colnames(mat) <- c("b_ols", "b_nnr", "iter_ols", "iter_nnr",
                     "conv_ols", "conv_nnr", "diff")
  mat
}

cat("\n========== Section 6: OLS vs NNR Init (N=50, T=50, fill=0.5, r=3) ==========\n")
mat_nnr <- run_ols_vs_nnr(N = 50L, TT = 50L, r = 3L, beta = beta_true,
                           B = B, nc = nc)

tab_nnr <- data.frame(
  Metric        = c("Bias", "RMSE", "Conv. rate (%)",
                    "Median iterations", "Mean |b_OLS - b_NNR|"),
  OLS.init      = c(
    fmt(mean(mat_nnr[, "b_ols"] - beta_true, na.rm = TRUE)),
    fmt(sqrt(mean((mat_nnr[, "b_ols"] - beta_true)^2, na.rm = TRUE))),
    fmt(100 * mean(mat_nnr[, "conv_ols"], na.rm = TRUE), 1L),
    fmt(median(mat_nnr[, "iter_ols"], na.rm = TRUE), 0L),
    fmt(mean(mat_nnr[, "diff"], na.rm = TRUE), 6L)
  ),
  NNR.init      = c(
    fmt(mean(mat_nnr[, "b_nnr"] - beta_true, na.rm = TRUE)),
    fmt(sqrt(mean((mat_nnr[, "b_nnr"] - beta_true)^2, na.rm = TRUE))),
    fmt(100 * mean(mat_nnr[, "conv_nnr"], na.rm = TRUE), 1L),
    fmt(median(mat_nnr[, "iter_nnr"], na.rm = TRUE), 0L),
    "-"
  ),
  stringsAsFactors = FALSE
)
cat("\n===== Table 6b: OLS vs NNR Initialization =====\n")
print(tab_nnr, row.names = FALSE)


